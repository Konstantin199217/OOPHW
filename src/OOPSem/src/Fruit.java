package OOPSem.src;/*Создать класс Fruit
 * В классе должно храниться следующая информация:
 *
 * 1 - название - name
 * 2 - цвет -color
 * 3 - вес - weight
 * 4 - стоимость - price*/

public class Fruit {
    String name;
    String color;
    double weight;
    double price;

    String getName(){
        return name;
    }
    String getColor(){
        return color;
    }
    double getWeight(){
        return weight;
    }
    double getPrice(){return price;}


}
